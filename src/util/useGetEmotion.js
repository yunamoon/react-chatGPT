
export function getEmotion (id) {
    switch(id) {
        case 1 :  
            return '^_^';
        case 2 :
            return '*^^*';
        case 3 :
            return '-_-';
        case 4: 
            return 'ㅜ_ㅜ';
        case 5 : 
            return 'ㅡㅡ';
        default:
             return null;
    };
};


